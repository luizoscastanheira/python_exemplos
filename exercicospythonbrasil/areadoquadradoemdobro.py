# Faça um Programa que calcule a área de um quadrado, 
# em seguida mostre o dobro desta área para o usuário

print("-" * 46)
base = float(input('Digite a base do quadrado: '))
altura = float(input('Agora digite a altura do quadrado: '))
print("-" * 46)
area = base * altura
print('A área do quadrado é de: ', area,'metros quadrados.')
print('Sua area em dobro corresponde a ', 2 * area, 'metros quadrados.')